import math
def area_triangle(base, height):
    return (base * height) / 2
def area_square(x, y):
    return x * y
def area_circle(diameter):
    return ((diameter/2)**2)*math.pi
def circumference_circle(diameter):
    return diameter * math.pi
def circumference_square(x, y):
    return (x * 2) + (y * 2)
